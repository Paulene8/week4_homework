tup = 'Hello'
print(len(tup))
# The variable tup is assigned the string 'Hello'. The len() function is then used to determine the length of the
# string, which is 5, because there are 5 characters in the string 'Hello'.

tup = 'Hello',
print(len(tup))
# In the second print statement, tup = 'Hello', assigns a tuple with a single element ('Hello',) to the variable tup,
# and print(len(tup)) prints the length of the tuple, which is 1, because it contains only one element.
